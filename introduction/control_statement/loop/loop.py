i=1
while i<=5:
  """
  1st iteration
  i=1,i<=5:True
  
  2nd iteration
  i=2,i<=5:True
  
  3rd iteration
  i=3,i<=5:True
  
  4th iteration
  i=4,i<=5:True
  
  5th iteration
  i=5,i<=5:True  
  
  6th iteration
  i=6,i<=5:False
  """
  print(i)
  i+=1
  
  
